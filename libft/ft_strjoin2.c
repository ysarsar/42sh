#include "libft.h"

void		ft_strjoin2(char **s1, char *s2)
{
	char *ptr;

	if (*s1 && s2)
	{
		ptr = *s1;
		*s1 = ft_strjoin(*s1, s2);
		ft_strdel(&ptr);
	}
}
