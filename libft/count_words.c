#include "libft.h"

int	count_words(char *str, char c)
{
	int		i;
	int		j;

	j = 0;
	i = 0;
	if (str[0] != c)
		j++;
	while (str[i])
	{
		if (str[i] != c && str[i - 1] == c)
			j++;
		i++;
	}
	return (j);
}
